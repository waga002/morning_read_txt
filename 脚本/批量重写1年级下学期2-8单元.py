def write_unit(unit_no: int, start_week: int) -> Path:
    out_path = OUT_DIR / f"1年级下册{unit_no}单元.txt"
    data = build_unit(unit_no, start_week)
    out_path.parent.mkdir(parents=True, exist_ok=True)
    out_path.write_text(json.dumps(data, ensure_ascii=False, indent="\t"), encoding="utf-8")
    return out_path


